/**
  *op_add - fuction thart add
  *@a: first int
  *@b: second int
  *Return: sum
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  *op_sub - this fuction sub
  *@a: first int
  *@b: second int
  *Return: sub
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  *op_mul - product of a and b
  *@a: first int
  *@b: second int
  *Return: mul
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  *op_div - this fuction div two int
  *@a: first int
  *@b: second int
  *Return: div
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  *op_mod - this function returns the remainder of the division of a,b
  *@a: first int
  *@b: secound int
  *Return: remainder
  */
int op_mod(int a, int b)
{
	return (a % b);
}
